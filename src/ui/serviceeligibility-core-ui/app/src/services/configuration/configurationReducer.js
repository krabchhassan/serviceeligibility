import types from './types';

/* ************************************* */
/* ********      VARIABLES      ******** */
/* ************************************* */
const initialState = {
    values: {},
};

/* ************************************* */
/* ********  PRIVATE FUNCTIONS  ******** */
/* ************************************* */

/* ************************************* */
/* ********   PUBLIC FUNCTIONS  ******** */
/* ************************************* */

/* ************************************* */
/* ********       EXPORTS       ******** */
/* ************************************* */
export default (state = initialState, action) => {
    switch (action.type) {
        case types.INIT_CONFIGURATION_FULFILLED.type:
            return {
                ...state,
                values: action.payload,
            };
        default:
            return state;
    }
};
