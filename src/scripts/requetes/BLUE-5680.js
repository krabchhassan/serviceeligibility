stringToDate = function (str) {
    var myDateString = new Date();
    if (str !== undefined) {
        myDateString.setFullYear(str.substr(0, 4));
        myDateString.setMonth(str.substr(5, 2) - 1);
        myDateString.setUTCDate(str.substr(8, 2));
        myDateString.setHours(12);
        myDateString.setMinutes(0);
        myDateString.setSeconds(0);
        myDateString.setMilliseconds(0);
    }
    return myDateString;
}
